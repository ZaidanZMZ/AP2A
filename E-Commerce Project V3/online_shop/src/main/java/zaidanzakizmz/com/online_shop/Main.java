    private void tambahProduk() {
        String id = idProduk.getText();
        String nama = namaProduk.getText();
        String hargaStr = harga.getText();
        String kat = (String) kategori.getSelectedItem();
        String ws = wors.getText();

        // Tambah ke list
        String[] data = {id, nama, hargaStr, kat, ws};
        produkList.add(data);

        // Tambah ke tabel
        produkModel.addRow(data);

        // Tambah ke combobox nmProduk
        nmProduk.addItem(nama);

        // Kosongkan input
        idProduk.setText("");
        namaProduk.setText("");
        harga.setText("");
        wors.setText("");
    }
    
    private void beliProduk() {
        String customer = cust.getText();
        String produkNama = (String) nmProduk.getSelectedItem();

        String[] data = {customer, produkNama};
        custList.add(data);

        custModel.addRow(data);

        // Kosongkan input
        cust.setText("");
    }




